"""# 1D CNN

Build sliding windows

Transforming the time-ordered tabular data into local sequences so a 1D CNN can learn neighbourhood patterns.
"""

seq_features = [
    "tb_h", "tb_v", "incidence", "nedt_h", "nedt_v",
    "tb_diff", "tb_h_roll_kurt", "tb_v_roll_kurt", "tb_diff_roll_kurt"
]

seq_df = df[seq_features + ["target"]].copy().reset_index(drop=True)

window_size = 9
X_seq = []
y_seq = []

for i in range(len(seq_df) - window_size + 1):
    window_block = seq_df.iloc[i:i+window_size]
    X_seq.append(window_block[seq_features].values)
    y_seq.append(window_block["target"].iloc[window_size // 2])

X_seq = np.array(X_seq)
y_seq = np.array(y_seq)

print("X_seq shape:", X_seq.shape)
print("y_seq shape:", y_seq.shape)

"""Split sequence data

Splitting the sequence dataset into training and testing sets.
"""

X_train_seq, X_test_seq, y_train_seq, y_test_seq = train_test_split(
    X_seq, y_seq, test_size=0.2, random_state=42, stratify=y_seq
)

"""Scale sequence data

Standardising sequence features before CNN training.
"""

n_train, w, f = X_train_seq.shape
n_test = X_test_seq.shape[0]

X_train_seq_2d = X_train_seq.reshape(-1, f)
X_test_seq_2d = X_test_seq.reshape(-1, f)

scaler_seq = StandardScaler()
X_train_seq_scaled = scaler_seq.fit_transform(X_train_seq_2d).reshape(n_train, w, f)
X_test_seq_scaled = scaler_seq.transform(X_test_seq_2d).reshape(n_test, w, f)

print(X_train_seq_scaled.shape, X_test_seq_scaled.shape)

"""1D CNN model

Trainning a sequence-aware convolutional neural network on the sliding-window data.
"""

cnn_1d = Sequential([
    Conv1D(filters=32, kernel_size=3, activation="relu", input_shape=(w, f)),
    MaxPooling1D(pool_size=2),
    Conv1D(filters=64, kernel_size=3, activation="relu"),
    GlobalAveragePooling1D(),
    Dense(32, activation="relu"),
    Dropout(0.3),
    Dense(1, activation="sigmoid")
])

cnn_1d.compile(
    optimizer="adam",
    loss="binary_crossentropy",
    metrics=["accuracy"]
)

early_stop_cnn = EarlyStopping(
    monitor="val_loss",
    patience=5,
    restore_best_weights=True
)

history_cnn = cnn_1d.fit(
    X_train_seq_scaled, y_train_seq,
    validation_split=0.2,
    epochs=30,
    batch_size=128,
    callbacks=[early_stop_cnn],
    verbose=1
)

"""Evaluate 1D CNN

Assessing the performance of the sequence-aware CNN.
"""

