    y_pred_rf_sub = rf_sub.predict(X_test_k)
    y_prob_rf_sub = rf_sub.predict_proba(X_test_k)[:, 1]
    subset_results.append(metric_row_subset(
        "RF + Kurtosis", frac, y_test_k, y_pred_rf_sub, y_prob_rf_sub
    ))

    # MLP + Kurtosis
    X_sub_mlp, y_sub_mlp = stratified_subset(X_train_k, y_train_k, frac)
    print("MLP subset:", X_sub_mlp.shape)

    scaler_sub_mlp = StandardScaler()
    X_sub_mlp_scaled = scaler_sub_mlp.fit_transform(X_sub_mlp)
    X_test_k_scaled_sub = scaler_sub_mlp.transform(X_test_k)

